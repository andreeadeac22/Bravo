class MLModel:
    """A class used for machine learning models."""
    
    def train(images):
        "Takes a set of images and trains a machine learning model"
    
    def predict(images):
        "Takes a sequences of images and applies model to predict next image in sequence."
    
    def save(filename):
        "Saves the machine learning model to filename."
    
    def load(filename):
        "Loads the machine learning model from filename."
